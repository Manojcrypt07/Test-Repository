package JavaPackager;

public class DemoDataTypes {

		
		public static void main (String[] args)
		{
			int a=67;
			char b='h';
			float c=66.45f;
			boolean e=false;
			double f=8888.4;
			
			System.out.println(a);
			System.out.println(b);
			System.out.println(c);
			System.out.println(e);
			System.out.println(f);
					
		}


}
