package JavaPackager;

public class ForEachDemo {

	public static void main(String[] args) {
			int a[] = {1,2,3,4,5,6};
			String str[]= {"ais", "joe", "arun","vinu"};
			
			
			for (int b : a) {
				System.out.println(b);
			}
			for (String str2 : str) {
				System.out.println(str2);
			}
	}

}
